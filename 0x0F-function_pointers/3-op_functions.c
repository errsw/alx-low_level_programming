#include "3-calc.h"

/**
* op_add - adds integers.
* @a: int.
* @b: int.
* Return: sum.
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
* op_sub - difference of integers.
* @a: int.
* @b: int.
* Return: difference.
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - Multipliates integers.
* @a: int.
* @b: int.
* Return: multiplication.
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - the division of integers.
* @a: int.
* @b: int.
* Return: division.
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - Returns the rest of division.
* @a: int.
* @b: int.
* Return: The rest of divisions.
*/
int op_mod(int a, int b)
{
	return (a % b);
}
